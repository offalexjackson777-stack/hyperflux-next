from __future__ import annotations

from typing import Any


def markdown(policy: dict[str, Any]) -> str:
    rows = "\n".join(
        f"| `{rule['path']}` | `{rule['expression']}` | {rule['reason']} |"
        for rule in policy["rules"]
    )
    texts = "\n".join(
        f"- `{expression}`: [`{path}`](../../{path})"
        for expression, path in sorted(policy["license_texts"].items())
    )
    return f"""# Licensing

> Generated by `./hfx generate` from `governance/licensing.json`. Do not edit manually.

{policy['rationale']}

The repository root has one detected project license: `GPL-2.0-only`. Narrow,
file-declared exceptions are resolved by the following path policy.

## Path Policy

| Path | SPDX expression | Reason |
| --- | --- | --- |
{rows}

## Included License Texts

{texts}

Every source file with an SPDX header is checked against this policy during
verification. Imported material still requires recorded origin, revision,
license, transformation, and migration disposition.
"""
