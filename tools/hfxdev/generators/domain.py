    lines.extend(["", "## Enumerations", ""])
    for enum in catalog["enums"]:
        values = ", ".join(f"`{value}`" for value in enum["values"])
        lines.append(f"- `{enum['name']}`: {values}")
    return "\n".join(lines) + "\n"
