def containerfile(environment: DevelopmentEnvironment) -> str:
    package_targets = (" " + "\\" + "\n    ").join(
        f"{package.name}={package.version}" for package in environment.packages
    )
    components = ",".join(environment.rust_components)
    timeout_policy = (
        "    && printf '%s\\n' 'DisableDownloadTimeout' >> /etc/pacman.conf \\\n"
        if environment.archive_disable_low_speed_timeout
        else ""
    )
    return f"""# Generated by ./hfx generate. Do not edit manually.
# SPDX-License-Identifier: GPL-2.0-only

ARG HFX_TARGET_PLATFORM={environment.platform}
FROM --platform=${{HFX_TARGET_PLATFORM}} {environment.image}

ARG HFX_ARCHIVE_MIRROR='{environment.archive_mirror}'
ENV RUSTUP_HOME=/opt/rustup \\
    CARGO_HOME=/opt/cargo \\
    PATH=/opt/cargo/bin:$PATH \\
    PIP_NO_INDEX=1 \\
    CARGO_NET_OFFLINE=true

RUN printf '%s\\n' "Server = ${{HFX_ARCHIVE_MIRROR}}" > /etc/pacman.d/mirrorlist \\
{timeout_policy}    && attempt=1 \\
    && until pacman -Syu --noconfirm --needed \\
    {package_targets} \\
    ; do \\
        test "$attempt" -lt {environment.archive_download_attempts} || exit 1; \\
        attempt=$((attempt + 1)); \\
        sleep 5; \\
    done \\
    && pacman -Scc --noconfirm

RUN install -d -m 0755 /opt/rustup /opt/cargo \\
    && rustup set profile {environment.rust_profile} \\
    && rustup toolchain install {environment.rust_toolchain} --component {components} \\
    && rustup default {environment.rust_toolchain} \\
    && chmod -R a+rX /opt/rustup /opt/cargo

RUN useradd --create-home --uid {environment.workspace_uid} --shell /bin/bash {environment.workspace_user} \\
    && printf '%s ALL=(ALL) NOPASSWD: ALL\\n' '{environment.workspace_user}' > /etc/sudoers.d/{environment.workspace_user} \\
    && chmod 0440 /etc/sudoers.d/{environment.workspace_user} \\
    && install -d -o {environment.workspace_uid} -g {environment.workspace_uid} /workspaces

USER {environment.workspace_user}
WORKDIR {environment.workspace_path}

CMD ["/bin/bash"]
"""


def devcontainer(environment: DevelopmentEnvironment) -> str:
    value: dict[str, Any] = {
        "name": "HyperFlux Next",
        "build": {
            "dockerfile": "Containerfile",
            "context": "..",
        },
        "remoteUser": environment.workspace_user,
        "updateRemoteUserUID": True,
        "workspaceFolder": environment.workspace_path,
        "postCreateCommand": networked_software_prepare_command(),
        "hostRequirements": {
            "cpus": 4,
            "memory": "8gb",
            "storage": "24gb",
        },
        "containerEnv": {
            "CARGO_HOME": f"{environment.workspace_path}/{CARGO_CACHE_PATH}",
            "CARGO_NET_OFFLINE": "true",
            "PIP_NO_INDEX": "1",
            "RUSTUP_HOME": "/opt/rustup",
            "RUSTUP_TOOLCHAIN": environment.rust_toolchain,
        },
    }
    return json.dumps(value, indent=2, sort_keys=True) + "\n"


def markdown(environment: DevelopmentEnvironment, upstreams: list[dict[str, Any]]) -> str:
    lines = [
        "# Reproducible Development Environment",
        "",
        "> Generated by `./hfx generate`. Do not edit manually.",
        "",
        "The development container is an immutable, non-privileged software build environment. "
        "It mounts no receiver or HID device and grants no hardware-write authority.",
        "",
        "## Identity",
        "",
        f"- Platform: `{environment.platform}`",
        f"- Base image: `{environment.image}`",
        f"- Arch package snapshot: `{environment.archive_date}`",
        f"- Rust toolchain: `{environment.rust_toolchain}`",
        "- Workspace: repository root (`hyperflux-next`)",
        "",
        "## Start",
        "",
        "Open the repository in a Development Containers-compatible editor, or build "
        "`.devcontainer/Containerfile` with an OCI container tool. The post-create step runs:",
        "",
        "```sh",
        " ".join(UPSTREAM_PREPARE_COMMAND),
        "CARGO_NET_OFFLINE=false " + " ".join(LOCKED_RUST_FETCH_COMMAND),
        "```",
        "",
        "This is the only ordinary setup step that uses the network. It creates clean, detached "
        "upstream checkouts and fetches the exact `Cargo.lock` dependency set into the "
        f"workspace-local `{CARGO_CACHE_PATH}/` cache. The reviewed upstream commits are:",
        "",
        "| Upstream | Commit | Contract |",
        "| --- | --- | --- |",
    ]
    for upstream in upstreams:
        lines.append(
            f"| {upstream['name']} | `{upstream['commit']}` | {upstream['api_contract']} |"
        )
    lines.extend(
        [
            "",
            "Then run either software lane without network access:",
            "",
            "```sh",
            "./hfx verify --fast",
            "./hfx verify --all",
            "```",
            "",
            "## Network Boundary",
            "",
            "Container construction may read the immutable Arch snapshot and install the exact "
            "Rust toolchain. Post-create preparation may fetch only cataloged repositories and "
            "locked Rust crates. Verification reuses those workspace-local inputs without network "
            "access; missing, dirty, or mismatched inputs fail with a remediation command.",
            "",
            f"Archive package retrieval permits up to {environment.archive_download_attempts} "
            "attempts. Low-speed transfer expiry is "
            f"{'disabled' if environment.archive_disable_low_speed_timeout else 'enabled'}. "
            "Package signatures and exact-version constraints remain mandatory.",
            "",
            "## System Package Pins",
            "",
            "| Package | Version |",
            "| --- | --- |",
        ]
    )
    lines.extend(f"| `{package.name}` | `{package.version}` |" for package in environment.packages)
    return "\n".join(lines) + "\n"
