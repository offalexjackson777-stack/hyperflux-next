from __future__ import annotations

from typing import Any


GENERATED_NOTICE = "> Generated by `./hfx generate`. Do not edit manually."


def shadow_comparison_markdown(fixture: dict[str, Any]) -> str:
    provenance = fixture["provenance"]
    decisions = fixture["legacy_decisions"]
    fields = (
        ("Profile selection", "selected_profiles"),
        ("Presence state", "presence_states"),
        ("Capabilities", "capabilities"),
        ("Transaction validation", "transaction_validation"),
        ("Diagnostic findings", "diagnostic_findings"),
    )
    lines = [
        "# Migration Shadow Comparison",
        "",
        GENERATED_NOTICE,
        "",
        "HyperFlux Next compares sanitized decisions recorded from the engineering implementation with the real new simulator and generated profile catalog. It never runs the legacy writer, opens a hardware endpoint, or grants hardware authority.",
        "",
        "## Canonical Comparison",
        "",
        f"- Comparison: `{fixture['comparison_id']}`",
        f"- Scenario: `{fixture['scenario']['scenario_id']}`",
        f"- Legacy source: `{provenance['source_id']}` at `{provenance['source_commit']}`",
        f"- Recorded source blobs: {len(provenance['source_records'])}",
        f"- Simulator events: {len(fixture['scenario']['events'])}",
        f"- Decision checkpoints: {len(decisions)}",
        "- Comparison mode: read-only recorded decisions",
        "- Hardware queries: forbidden",
        "- Hardware writes: forbidden",
        "- Publication authority: none",
        "",
        "## Semantic Coverage",
        "",
        "| Domain | Compared checkpoints |",
        "| --- | ---: |",
    ]
    for title, key in fields:
        count = sum(key in decision for decision in decisions)
        lines.append(f"| {title} | {count} |")
    lines.extend(
        [
            "",
            "## Source Binding",
            "",
            "Every legacy decision is tied to exact Git objects already present in the frozen migration inventory:",
            "",
        ]
    )
    for record in provenance["source_records"]:
        lines.append(f"- `{record['path']}` at `{record['object']}`")
    lines.extend(
        [
            "",
            "## Run Locally",
            "",
            "```bash",
            "./hfx migration compare \\",
            "  --fixture tests/fixtures/shadow/qualified-lifecycle-v1.json \\",
            "  --output build/shadow-comparison",
            "```",
            "",
            "The output contains `comparison.json` with typed checkpoint differences and `evidence.json` binding the result to the current Git revision, fixture, legacy inventory, and generated profile catalog.",
            "",
            "A match establishes software parity only for the recorded decisions. It does not replace physical qualification or authorize publication.",
            "",
        ]
    )
    return "\n".join(lines)
